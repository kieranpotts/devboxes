#!/bin/bash

# ------------------------------------------------------------------------------
# Announce the booting up of applications.
# ------------------------------------------------------------------------------

read -r -d '' msg << EOF
┌──────────────────────────────────────────────────────────────────────────────┐
│                                                                              │
│   BOOTING UP                                                                 │
│                                                                              │
│   Starting applications.                                                     │
│                                                                              │
└──────────────────────────────────────────────────────────────────────────────┘
EOF

echo "${msg}"
